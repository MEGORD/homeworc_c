'''
Создайте список из 12 элементов.
Каждый элемент этого списка представляет собой
зарплату рабочего за месяц (случайное число от
7500 до 15000 грн.). Выведите этот список на экран
и вычислите среднемесячную зарплату этого
рабочего.
'''
import random
arr = []

for i in range(12):
    arr.append(random.randint(7500, 15000))
print(arr)
mean = int(sum(arr) / len(arr))
print(mean)
