'''
Дан список [0, 5, 2, 4, 7, 1, 3, 19].
Написать программу для подсчета нечетных
элементов в нем.
'''
arr = [0, 5, 2, 4, 7, 1, 3, 19]
sum_el = 0

for i in arr:
    if not i % 2 == 0:
        sum_el += i
print('Summa of elements:', sum_el)
'''
Создайте список случайных чисел (размером 4 элемента).
Создайте второй список в два раза больше первого,
где первые 4 элемента должны быть равны элементам
первого списка, а остальные заполните удвоенныемы
значенгиями начальных.
'''
import random
arr = []
arr_s = []

for i in range(4):
    arr.append(random.randint(1, 10))
arr_s = arr * 2
for i in range(4, 8):
    arr_s[i] = arr_s[i] * 2
print(arr)
